var isPalindrome = function(str) {
    var forward = str.toString();
    var reverse = forward.split('').reverse().join('');;
    return forward === reverse;
};

var largestPairProduct = function(max, filter = alwaysTrue) {
    var a = max;
    var b = max;
    var prod = 0;
    for (let i = max; i > 0; i--) {
        if (i * max < prod) break;
        for (let j = max; j >= i; j--) {
            if (i * j < prod) break;
            if (filter(i * j)) {
                prod = i * j;
            }
        }
    }
    return prod;
}

